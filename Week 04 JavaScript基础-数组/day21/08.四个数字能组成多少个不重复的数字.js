// 需求：	有1、2、3、4个数字，能组成多少个互不相同且无重复数字的三位数？有多少个，有哪些三位数？



//定义三位数的个、十、百位
var a = 0,b = 0,c=0
//定义三位数的个数
var num = 0
//循环定义百位
for (var a = 1; a <= 4; a++) {
  //循环定义十位
  for (var b = 1; b <= 4; b++) {
    //循环定义个位
    for (var c = 1; c <= 4; c++) {
      //满足个十百不同(不重复)
      if(a != b && a != c && b != c ){
        //满足条件，自增1，定义三位数个数
        num ++
        //输出三位数
        console.log(`${a}${b}${c}`);
        
      }
    }
  }
}
//输出满足三位数的个数
console.log(num);