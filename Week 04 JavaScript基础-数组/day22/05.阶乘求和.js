// 需求:控制台输出：计算 1+1/2!+1/3!+1/4!+...1/20!的和

// 分析:分子不变,分母是阶乘
//     1x1=1        1!
//     1x2=2        2!
//     1x2x3=6      3!
//     1x2x3x4=24   4!

//     先弄求阶乘的for循环


var sum = 1;//阶乘从1开始
var jie_sum = 0;
for (var i = 1; i <= 20; i++) {//求1到20的阶乘

    sum *= i;//求阶乘

    jie_sum += 1 / sum;//求阶乘和
}
console.log(jie_sum);