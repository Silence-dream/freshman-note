// 假设某人有100,000现金。每经过一次路口需要进行一次交费。交费规则为当他现金大于50,000时每次需要交5%如果现金小于等于50,000时每次交5,000。请写一程序计算此人可以经过多少次这个路口


// 分析：
//     1.定义个变量存现金
//     2.定义个变量存次数
//     3.需要用循环和判断语句写


var money = 100000;
var i = 1;//累加次数
for (i = 1; money > 5000; i++) {
    if (money > 50000) {
        // money = money - money * 0.05
        money -= money * 0.05;
    }
    else {
        // money = money - 5000
        money -= 5000;
    }
}
console.log("他一共过了" + i + "次路口," + "还剩" + money + "元钱");



