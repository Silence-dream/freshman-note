// 需求: 鸡兔同笼：鸡和兔子一共35只，笼子里一共有94只脚，用程序计算出鸡和兔子分别多少只

// 分析：鸡有2腿
//       兔有4腿
//  鸡和兔子总共35只
//    鸡的腿数*2+兔的腿数*4=94 
//    鸡+兔=35



//定义鸡和兔的腿
var ji=2;
var tu=4;

for(var ji_T=1;ji_T<=35;ji_T++)//这个循环历遍鸡
{
    for(var tu_T=1;tu_T<=35;tu_T++)//这个循环历遍兔子
    {
        if(ji*ji_T+tu*tu_T==94 && ji_T+tu_T==35)//鸡的腿数*2+兔的腿数*4=94  鸡+兔=35
        {
            console.log(`鸡的数量是${ji_T}，兔的数量是${tu_T}`);
        }
    }
}

